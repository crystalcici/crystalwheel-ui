<template>
  <div>
    Tabs组件
    <component v-for="items in defaults" :is="items" :key="items"/>
  </div>
</template>
<script lang="ts">
import Tab from './TabProp.vue'
export default {
  setup(props, context) {
    const defaults = context.slots.default()
    defaults.forEach((tag) => {
      if(tag.type !== Tab){
        throw new Error('Tabs子标签必须是Tab')
      }
    })
  }
}
</script>